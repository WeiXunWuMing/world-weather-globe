  /**
   * Draw technical Bauhaus forecast line graph
   */
  function drawForecastGraph(forecast) {
    const fCanvas = document.getElementById('forecast-canvas');
    const fCtx = fCanvas.getContext('2d');
    
    const w = fCanvas.width;
    const h = fCanvas.height;
    
    fCtx.clearRect(0, 0, w, h);
    
    // Find min and max temperatures for bounding scales
    const temps = forecast.map(f => f.temp);
    const minTemp = Math.min(...temps) - 1.5;
    const maxTemp = Math.max(...temps) + 1.5;
    const tempDiff = maxTemp - minTemp || 1.0;
    
    fCtx.strokeStyle = 'rgba(255, 255, 255, 0.12)';
    fCtx.lineWidth = 0.8;
    
    // Draw grid bounds
    const gridCols = 5;
    const colWidth = w / gridCols;
    
    // Horizontal bounds line
    fCtx.beginPath();
    fCtx.moveTo(0, h - 18);
    fCtx.lineTo(w, h - 18);
    fCtx.stroke();
    
    // Map data points
    const points = [];
    for (let i = 0; i < forecast.length; i++) {
      const fx = colWidth * i + (colWidth / 2);
      const tempRatio = (forecast[i].temp - minTemp) / tempDiff;
      const fy = h - 22 - (tempRatio * (h - 38)); // Map coordinates flipped inside canvas
      points.push({ x: fx, y: fy, label: forecast[i].time, temp: forecast[i].temp });
    }
    
    // Draw continuous data paths
    fCtx.strokeStyle = '#ffffff';
    fCtx.lineWidth = 1.2;
    fCtx.beginPath();
    fCtx.moveTo(points[0].x, points[0].y);
    for (let i = 1; i < points.length; i++) {
      fCtx.lineTo(points[i].x, points[i].y);
    }
    fCtx.stroke();
    
    // Draw dots and technical numeric metrics at nodes
    fCtx.fillStyle = '#ffffff';
    fCtx.font = '8px var(--font-tech)';
    fCtx.textAlign = 'center';
    
    for (const pt of points) {
      // Small square dot representing Bauhaus aesthetics
      fCtx.fillRect(pt.x - 2, pt.y - 2, 4, 4);
      
      // Temp data text above
      fCtx.fillText(`${pt.temp.toFixed(0)}°`, pt.x, pt.y - 8);
      
      // Time label ticks below grid bounds line
      fCtx.fillStyle = 'rgba(255, 255, 255, 0.45)';
      fCtx.fillText(pt.label, pt.x, h - 6);
      fCtx.fillStyle = '#ffffff';
    }
  }

