  /**
   * Ray-casting point-in-polygon algorithm for high-fidelity province boundary hit-testing.
   */
  function isPointInPolygon(x, y, ring) {
    let inside = false;
    for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
      const xi = ring[i][0], yi = ring[i][1];
      const xj = ring[j][0], yj = ring[j][1];
      const intersect = ((yi > y) !== (yj > y))
          && (x < (xj - xi) * (y - yi) / (yj - yi) + xi);
      if (intersect) inside = !inside;
    }
    return inside;
  }

  /**
   * Reusable parent city coordinate projection resolver (polar grid structure)
   */
  function getParentCityCoords(prov, cityName, isUS) {
    const cities = prov.cities;
    let found = false;
    let targetLon = prov.centroid[0];
    let targetLat = prov.centroid[1];
    const maxDist = isUS ? 2 : 3;
    const maxAng = isUS ? 6 : 8;
    for (let distIdx = 0; distIdx < maxDist; distIdx++) {
      for (let angIdx = 0; angIdx < maxAng; angIdx++) {
        const hash = isUS ? 
          ((angIdx * 5 + distIdx * 11) % cities.length) :
          ((angIdx * 7 + distIdx * 13) % cities.length);
        if (cities[hash] === cityName) {
          const angle = ((angIdx + 0.5) / maxAng) * 2 * Math.PI - Math.PI;
          const distance = (distIdx + 0.5) / maxDist;
          targetLon = prov.centroid[0] + distance * Math.cos(angle);
          targetLat = prov.centroid[1] + distance * Math.sin(angle);
          found = true;
          break;
        }
      }
      if (found) break;
    }
    return [targetLat, targetLon];
  }

